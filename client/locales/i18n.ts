// 语言存储键
const LANGUAGE_KEY = '@voxora_language';

// 初始化 i18n
const initI18n = async () => {
  // 尝试从 AsyncStorage 读取保存的语言
  let savedLanguage = await AsyncStorage.getItem(LANGUAGE_KEY);

  // 如果没有保存的语言，使用设备语言
  if (!savedLanguage) {
    const deviceLanguage = getLocales()[0]?.languageCode || 'zh';
    // 映射设备语言到支持的语言
    savedLanguage = mapDeviceLanguageToSupported(deviceLanguage);
  }

  await i18n.use(initReactI18next).init({
    resources: {
      'zh-CN': { translation: zhCN },
      'en': { translation: en },
      'hi': { translation: hi },
    },
    lng: savedLanguage,
    fallbackLng: 'zh-CN',
    interpolation: {
      escapeValue: false,
    },
  });

  return savedLanguage;
};

// 映射设备语言到支持的语言
const mapDeviceLanguageToSupported = (deviceLanguage: string): string => {
  const langMap: Record<string, string> = {
    'zh': 'zh-CN',
    'en': 'en',
    'hi': 'hi',
  };
  return langMap[deviceLanguage] || 'zh-CN';
};

// 切换语言
const changeLanguage = async (language: string) => {
  await i18n.changeLanguage(language);
  await AsyncStorage.setItem(LANGUAGE_KEY, language);
};

// 获取当前语言
const getCurrentLanguage = (): string => {
  return i18n.language;
};

// 导出
export { initI18n, changeLanguage, getCurrentLanguage };
export default i18n;
